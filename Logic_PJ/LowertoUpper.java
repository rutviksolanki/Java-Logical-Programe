// Question: Write Java Program Convert String LowerCase to UpperCase.
package Logic_PJ;
public class LowertoUpper {
	public static void main(String[] args) {
		String sentence = "Hi I'm Full Stack Developer.";
		System.out.println(sentence);
		String sentenceUpper = sentence.toUpperCase();
		System.out.print(sentenceUpper);
	}
}
