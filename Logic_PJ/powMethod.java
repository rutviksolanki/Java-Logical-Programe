// Question: Write Java Program to Find Power of Given Number Using Function.
package Logic_PJ;
import java.util.Scanner;
public class powMethod {
	public static void main(String[] args) {
		int base = 2;
		int power = 3;
		double ans = Math.pow(base, power);
		System.out.println(base + "^" + power + " = " + ans);
	}
}
